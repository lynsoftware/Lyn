using Lyn.Backend.Configuration;

// ============================================== 1. BUILDER CONFIGURATION ==============================================
var builder = WebApplication.CreateBuilder(args);


// ============================================== 2. SERVICES ==============================================
builder.ConfigureServices();


// ============================================== 3. APP CONFIGURATION ==============================================
var app = builder.Build();


// ============================================== 4. MIDDLEWARE PIPELINE & ENDPOINTS ==============================================
app.ConfigureMiddleware();


// ============================================== 5. RUN ==============================================
app.Run();